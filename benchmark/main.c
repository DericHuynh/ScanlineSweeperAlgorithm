/* =========================================================================
 * Shader building helper
 * ========================================================================= */
static GLuint compile_shader(GLenum type, const char *src)
{
    GLuint s = glCreateShader(type);
    glShaderSource(s, 1, &src, NULL);
    glCompileShader(s);
    GLint ok;
    glGetShaderiv(s, GL_COMPILE_STATUS, &ok);
    if (!ok) {
        char log[4096];
        glGetShaderInfoLog(s, sizeof(log), NULL, log);
        fprintf(stderr, "Shader error (type=0x%x):\n%s\n", type, log);
        /* Print source around error */
        fprintf(stderr, "--- source (first 2000 chars) ---\n%.2000s\n", src);
        exit(1);
    }
    return s;
}

static GLuint link_program(GLuint vs, GLuint cs_or_fs)
{
    GLuint p = glCreateProgram();
    if (vs) glAttachShader(p, vs);
    glAttachShader(p, cs_or_fs);
    glLinkProgram(p);
    GLint ok;
    glGetProgramiv(p, GL_LINK_STATUS, &ok);
    if (!ok) {
        char log[4096];
        glGetProgramInfoLog(p, sizeof(log), NULL, log);
        fprintf(stderr, "Link error:\n%s\n", log);
        exit(1);
    }
    return p;
}

/* Build glyph sweep compute shader source, substituting placeholders. */
static char *build_sweep_cs_source(int accurate)
{
    const char *bezi = BEZIER_SHADER_SRC;
    const char *swep = SWEEP_SHADER_SRC;
    const char *tmpl = GLYPH_CS_TEMPLATE;
    size_t len = strlen(tmpl) + strlen(bezi) + strlen(swep) + strlen(COMMON_GLSL) + 256;
    char *buf = malloc(len);
    if (!buf) return NULL;

    char *d = buf;
    const char *s = tmpl;
    while (*s) {
        if (strncmp(s, "%%ACCURATE%%", 12) == 0) {
            *d++ = accurate ? '1' : '0';
            s += 12;
        } else if (strncmp(s, "COMMON_PLACEHOLDER", 18) == 0) {
            size_t n = strlen(COMMON_GLSL);
            memcpy(d, COMMON_GLSL, n); d += n;
            s += 18;
        } else if (strncmp(s, "%%BEZIER%%", 10) == 0) {
            size_t n = strlen(bezi);
            memcpy(d, bezi, n); d += n;
            s += 10;
        } else if (strncmp(s, "%%SWEEP%%", 9) == 0) {
            size_t n = strlen(swep);
            memcpy(d, swep, n); d += n;
            s += 9;
        } else {
            *d++ = *s++;
        }
    }
    *d = '\0';
    return buf;
}

/* =========================================================================
 * Math / geometry helpers
 * ========================================================================= */
typedef struct { float x, y; } V2;

typedef struct {
    V2 p0, p1, p2;
} BezierCurve;

typedef struct {
    BezierCurve *data;
    int count, cap;
} CurveList;

static void cl_init(CurveList *cl, int cap) {
    cl->data = malloc(sizeof(BezierCurve) * (size_t)cap);
    cl->count = 0; cl->cap = cap;
}
static void cl_push(CurveList *cl, BezierCurve c) {
    if (cl->count >= cl->cap) {
        cl->cap *= 2;
        cl->data = realloc(cl->data, sizeof(BezierCurve) * (size_t)cl->cap);
    }
    cl->data[cl->count++] = c;
}
static void cl_free(CurveList *cl) {
    free(cl->data); cl->data = NULL; cl->count = cl->cap = 0;
}

/* =========================================================================
 * Quadratic-Bézier subdivision → y-monotonic segments
 * ========================================================================= */
static int is_y_monotonic(float y0, float y1, float y2)
{
    float qa = y0 - 2.f*y1 + y2;
    if (fabsf(qa) < 1e-6f) return 1;
    float t = (y0 - y1) / qa;
    return t <= 0.f || t >= 1.f;
}

static void add_monotonic(CurveList *cl, V2 p0, V2 p1, V2 p2)
{
    if (is_y_monotonic(p0.y, p1.y, p2.y)) {
        cl_push(cl, (BezierCurve){p0, p1, p2});
    } else {
        float qa = p0.y - 2.f*p1.y + p2.y;
        float t  = (p0.y - p1.y) / qa;
        /* de Casteljau at t */
        V2 p01 = { (1.f-t)*p0.x + t*p1.x, (1.f-t)*p0.y + t*p1.y };
        V2 p12 = { (1.f-t)*p1.x + t*p2.x, (1.f-t)*p1.y + t*p2.y };
        V2 q   = { (1.f-t)*p01.x + t*p12.x, (1.f-t)*p01.y + t*p12.y };
        add_monotonic(cl, p0, p01, q);
        add_monotonic(cl, q, p12, p2);
    }
}

/* =========================================================================
 * FreeType outline decomposition
 * ========================================================================= */
typedef struct {
    CurveList *curves;
    V2 first, last;
    int has_first;
} DecompCtx;

static int ft_move_to(const FT_Vector *to, void *user) {
    DecompCtx *c = user;
    c->first.x = (float)to->x; c->first.y = (float)to->y;
    c->last = c->first; c->has_first = 1;
    return 0;
}
static int ft_line_to(const FT_Vector *to, void *user) {
    DecompCtx *c = user;
    V2 p0 = c->last, p2 = { (float)to->x, (float)to->y };
    V2 p1 = { 0.5f*(p0.x+p2.x), 0.5f*(p0.y+p2.y) };
    add_monotonic(c->curves, p0, p1, p2);
    c->last = p2;
    return 0;
}
static int ft_conic_to(const FT_Vector *ctrl, const FT_Vector *to, void *user) {
    DecompCtx *c = user;
    V2 p0 = c->last, p1 = {(float)ctrl->x,(float)ctrl->y}, p2 = {(float)to->x,(float)to->y};
    add_monotonic(c->curves, p0, p1, p2);
    c->last = p2;
    return 0;
}
static int ft_cubic_to(const FT_Vector *a, const FT_Vector *b, const FT_Vector *to, void *user) {
    /* TTF outlines are quadratic; cubics indicate an OTF/CFF font.
     * Crude approximation: use midpoint of a,b as single quadratic ctrl point. */
    DecompCtx *c = user;
    V2 p0 = c->last;
    V2 p1 = {0.5f*((float)a->x+(float)b->x), 0.5f*((float)a->y+(float)b->y)};
    V2 p2 = {(float)to->x, (float)to->y};
    add_monotonic(c->curves, p0, p1, p2);
    c->last = p2;
    return 0;
}

