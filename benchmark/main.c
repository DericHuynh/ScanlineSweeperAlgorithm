/* common.glsl preamble (mad/saturate macros) */
static const char *COMMON_GLSL =
    "#define mad(a,b,c) ((a)*(b)+(c))\n"
    "#define saturate(x) clamp((x),0.0,1.0)\n";

/* ---- Glyph compute-shader wrapper  (GLSL) ----
 *
 * This string contains two sentinels that we replace at runtime:
 *   %%ACCURATE%%  →  "0" or "1"
 *   %%BEZIER%%    →  contents of bezier_intersect.glsl
 *   %%SWEEP%%     →  contents of scanline_sweep.glsl          */
static const char *GLYPH_CS_TEMPLATE =
    "#version 430 core\n"
    "#define ACCURATE_COVERAGE %%ACCURATE%%\n"
    "\n"
    "layout(local_size_x = 8, local_size_y = 8) in;\n"
    "\n"
    "layout(std430, binding = 0) readonly buffer Curves { float c[]; };\n"
    "layout(rgba8, binding = 1) writeonly uniform image2D out_img;\n"
    "\n"
    "uniform int  curve_count;\n"
    "uniform int  glyph_w;\n"
    "uniform int  glyph_h;\n"
    "uniform int  dest_x;\n"
    "uniform int  dest_y;\n"
    "\n"
    "/* ---------- common.glsl ---------- */\n"
    "COMMON_PLACEHOLDER\n"
    "\n"
    "/* ---------- bezier_intersect.glsl ---------- */\n"
    "%%BEZIER%%\n"
    "\n"
    "/* ---------- scanline_sweep.glsl ---------- */\n"
    "%%SWEEP%%\n"
    "\n"
    "void main() {\n"
    "    ivec2 px = ivec2(gl_GlobalInvocationID.xy);\n"
    "    if (px.x >= glyph_w || px.y >= glyph_h) return;\n"
    "\n"
    "    vec2 pixel_offset = vec2(float(px.x), float(px.y));\n"
    "    vec2 pixel_size   = vec2(1.0);\n"
    "\n"
    "    float coverage = 0.0;\n"
    "    for (int i = 0; i < curve_count; i++) {\n"
    "        uint b = uint(i) * 6u;\n"
    "        vec2 p0 = vec2(c[b+0], c[b+1]);\n"
    "        vec2 p1 = vec2(c[b+2], c[b+3]);\n"
    "        vec2 p2 = vec2(c[b+4], c[b+5]);\n"
    "        coverage += scanline_sweep(pixel_size, pixel_offset, p0, p1, p2);\n"
    "    }\n"
    "\n"
    "    float alpha = clamp(coverage, 0.0, 1.0);\n"
    "    /* white glyph on transparent background */\n"
    "    imageStore(out_img, ivec2(dest_x + px.x, dest_y + px.y), vec4(alpha));\n"
    "}\n";

/* ---- Alternative: ray-stabber glyph compute shader  ---- */
static const char *GLYPH_RAYSTAB_CS =
    "#version 430 core\n"
    "#define SAMPLES 4\n"
    "\n"
    "layout(local_size_x = 8, local_size_y = 8) in;\n"
    "layout(std430, binding = 0) readonly buffer Curves { float c[]; };\n"
    "layout(rgba8, binding = 1) writeonly uniform image2D out_img;\n"
    "\n"
    "uniform int  curve_count;\n"
    "uniform int  glyph_w;\n"
    "uniform int  glyph_h;\n"
    "uniform int  dest_x;\n"
    "uniform int  dest_y;\n"
    "\n"
    "float solve_quadratic(float a, float b, float c,\n"
    "                       out float t0, out float t1) {\n"
    "    if (abs(a) < 1e-7) {\n"
    "        if (abs(b) < 1e-7) return 0.0;\n"
    "        t0 = -c / b;\n"
    "        return (t0 >= 0.0 && t0 <= 1.0) ? 1.0 : 0.0;\n"
    "    }\n"
    "    float d = b*b - 4.0*a*c;\n"
    "    if (d < 0.0) return 0.0;\n"
    "    float sd = sqrt(d);\n"
    "    float inv2a = 0.5 / a;\n"
    "    t0 = (-b - sd) * inv2a;\n"
    "    t1 = (-b + sd) * inv2a;\n"
    "    if (t0 > t1) { float tmp = t0; t0 = t1; t1 = tmp; }\n"
    "    int cnt = 0;\n"
    "    if (t0 >= 0.0 && t0 <= 1.0) cnt++;\n"
    "    if (t1 >= 0.0 && t1 <= 1.0) cnt++;\n"
    "    return float(cnt);\n"
    "}\n"
    "\n"
    "vec2 eval_bezier(vec2 p0, vec2 p1, vec2 p2, float t) {\n"
    "    float mt = 1.0 - t;\n"
    "    return mt*mt*p0 + 2.0*mt*t*p1 + t*t*p2;\n"
    "}\n"
    "\n"
    "void main() {\n"
    "    ivec2 px = ivec2(gl_GlobalInvocationID.xy);\n"
    "    if (px.x >= glyph_w || px.y >= glyph_h) return;\n"
    "\n"
    "    float hits = 0.0;\n"
    "    for (int sy = 0; sy < SAMPLES; sy++) {\n"
    "    for (int sx = 0; sx < SAMPLES; sx++) {\n"
    "        float rx = float(px.x) + (float(sx)+0.5)/float(SAMPLES);\n"
    "        float ry = float(px.y) + (float(sy)+0.5)/float(SAMPLES);\n"
    "\n"
    "        float xs[64]; int nx = 0;\n"
    "        for (int i = 0; i < curve_count; i++) {\n"
    "            uint b = uint(i)*6u;\n"
    "            vec2 p0 = vec2(c[b+0], c[b+1]);\n"
    "            vec2 p1 = vec2(c[b+2], c[b+3]);\n"
    "            vec2 p2 = vec2(c[b+4], c[b+5]);\n"
    "            float a  = p0.y - 2.0*p1.y + p2.y;\n"
    "            float bl = 2.0*(p1.y - p0.y);\n"
    "            float cl = p0.y - ry;\n"
    "            float t0, t1;\n"
    "            float nc = solve_quadratic(a, bl, cl, t0, t1);\n"
    "            if (nc >= 1.0 && nx < 64) {\n"
    "                xs[nx++] = eval_bezier(p0,p1,p2,t0).x;\n"
    "            }\n"
    "            if (nc >= 2.0 && nx < 64) {\n"
    "                xs[nx++] = eval_bezier(p0,p1,p2,t1).x;\n"
    "            }\n"
    "        }\n"
    "        /* bubble sort */\n"
    "        for (int j = 0; j < nx-1; j++)\n"
    "            for (int k = j+1; k < nx; k++)\n"
    "                if (xs[k] < xs[j]) {\n"
    "                    float t = xs[j]; xs[j]=xs[k]; xs[k]=t;\n"
    "                }\n"
    "        /* even-odd rule */\n"
    "        int inside = 0;\n"
    "        for (int j = 0; j < nx; j++) {\n"
    "            inside = 1 - inside;\n"
    "            if (inside != 0 && xs[j] < rx && (j+1>=nx || xs[j+1]>=rx)) {\n"
    "                hits += 1.0;\n"
    "                break;\n"
    "            }\n"
    "        }\n"
    "    }}\n"
    "\n"
    "    float alpha = hits / float(SAMPLES*SAMPLES);\n"
    "    imageStore(out_img, ivec2(dest_x + px.x, dest_y + px.y), vec4(alpha));\n"
    "}\n";

